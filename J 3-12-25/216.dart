// Iterable class

// 19] toSet() --> Set<E>
// Returns a string representation of (some of) the element of this.

void main() {
  var players = ["Rohit", "Shubhman", "Virat", "KL", "Shreyas", "Hardik"];

  var retVal = players.toSet();

  print(retVal);
}

/*

Output:



{Rohit, Shubhman, Virat, KL, Shreyas, Hardik}




*/
